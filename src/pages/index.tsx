import { Socials } from "../components/socials";

interface Post {
  title: string;
  slug: string;
  dateAdded: string;
  brief: string;
}
type Publication = {
  posts: Post[];
};
type User = {
  publication: Publication;
};

const fetchPosts = async (page: number): Promise<Post[]> => {
  const headers = {
    "content-type": "application/json",
  };
  const requestBody = {
    query: `query  fetchPosts($page: Int) {
      user(username:"kasp9023") {
         publication {
          posts(page: $page) {
            title
            slug
            dateAdded
            brief
          }
        } 
      }
    }`,
    variables: {
      page,
    },
  };
  const options = {
    method: "POST",
    headers,
    body: JSON.stringify(requestBody),
  };

  const response = await fetch("https://api.hashnode.com", options);
  const data = (await response.json()) as { data: { user: User } };

  const {
    data: {
      user: {
        publication: { posts },
      },
    },
  } = data;

  return posts;
};

